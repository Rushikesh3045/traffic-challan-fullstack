-- 1. DROP EXISTING TABLES (Reset)
DROP TABLE IF EXISTS payment CASCADE;
DROP TABLE IF EXISTS appeal CASCADE;
DROP TABLE IF EXISTS challan CASCADE;
DROP TABLE IF EXISTS violation CASCADE;
DROP TABLE IF EXISTS users CASCADE;

-- 2. USERS TABLE
CREATE TABLE users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    email VARCHAR(255) UNIQUE,
    name VARCHAR(255),
    role VARCHAR(50) NOT NULL -- ROLE_ADMIN, ROLE_POLICE, ROLE_CITIZEN
);

-- 3. VIOLATION TABLE
CREATE TABLE violation (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vehicle_number VARCHAR(255) NOT NULL,
    violation_type VARCHAR(255) NOT NULL,
    fine_amount DOUBLE PRECISION NOT NULL,
    location VARCHAR(255),
    description TEXT,
    status VARCHAR(50) DEFAULT 'UNPAID',
    gps_latitude VARCHAR(255),                                                                                              
    gps_longitude VARCHAR(255),
    evidence_image TEXT, -- Base64 string
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    paid_at TIMESTAMP
);

-- 4. CHALLAN TABLE
CREATE TABLE challan (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    violation_id BIGINT REFERENCES violation(id),
    vehicle_number VARCHAR(255),
    amount DOUBLE PRECISION,
    payment_status VARCHAR(50) DEFAULT 'UNPAID'
);

-- 5. APPEAL TABLE
CREATE TABLE appeal (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    challan_id BIGINT,
    reason TEXT,
    status VARCHAR(50) DEFAULT 'PENDING',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 6. PAYMENT TABLE
CREATE TABLE payment (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    challan_id BIGINT,
    payment_mode VARCHAR(50),
    payment_status VARCHAR(50),
    payment_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    payment_time VARCHAR(255)
);

